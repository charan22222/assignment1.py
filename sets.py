#sets

myset = {"apple","banana","mango"}
print(myset)

#access sets
myset = {"apple","banana","mango"}
for x in myset:
    print(x)

#add items
myset = {"apple","banana","mango"}
myset.add("orange")
print(myset)

#remove items
myset = {"apple","banana","mango"}
myset.remove("mango")
print(myset)

#Loops sets
myset = {"apple","banana","mango"}
for x in myset:
    print(x)
